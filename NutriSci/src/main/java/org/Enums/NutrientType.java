package org.Enums;

/**
 * Represents the type of nutrient given by the food item.
 */
public enum NutrientType {
    /**
     * Proteins item.
     */
    Protein,
    /**
     * Carbohydrate item.
     */
    Carbohydrate,
    /**
     * Fatty item.
     */
    Fat,
    /**
     * Fiber item.
     */
    Fiber,
    /**
     * The item gives other types of nutrients not listed here.
     */
    Other
}