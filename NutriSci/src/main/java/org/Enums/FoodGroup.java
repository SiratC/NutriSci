package org.Enums;

/**
 * Represents the food group of a food item.
 */
public enum FoodGroup {
    /**
     * Grain items.
     */
    Grains,
    /**
     * Vegetable items.
     */
    Vegetables,
    /**
     * Fruit items.
     */
    Fruit,
    /**
     * Protein items.
     */
    Protein,
    /**
     * Dairy items.
     */
    Dairy }
